/****************************************************************************
* 名	 称: void USER_DMA_Config(DMA_Stream_TypeDef *DMA_Streamx,u32 chx,u32 par,u32 mar,u16 ndtr)
* 功	 能：DMAx的各通道配置
* 入口参数：DMA_Streamx:DMA数据流,DMA1_Stream0~7/DMA2_Stream0~7
			chx:DMA通道选择,@ref DMA_channel DMA_Channel_0~DMA_Channel_7
					  par:外设地址
					  mar:存储器地址
					  ndtr:数据传输量						
* 返回参数：无
* 说	 明：这边传输形式是固定的,这点要根据不同的情况来修改 
****************************************************************************/
void USER_DMA_Config(DMA_Stream_TypeDef * DMA_Streamx, u32 chx, u32 par, u32 mar, u16 ndtr)
{
	DMA_InitTypeDef DMA_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;

	if ((u32) DMA_Streamx > (u32) DMA2) //得到当前stream是属于DMA2还是DMA1
		{
		RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_DMA2, ENABLE); //DMA2时钟使能 

		}
	else 
		{
		RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_DMA1, ENABLE); //DMA1时钟使能 
		}

	DMA_DeInit(DMA_Streamx);

	while (DMA_GetCmdStatus(DMA_Streamx) != DISABLE)
		{
		} //等待DMA可配置 

	/* 配置 DMA Stream */

	DMA_InitStructure.DMA_Channel = chx;			//通道选择
	DMA_InitStructure.DMA_PeripheralBaseAddr = par; //DMA外设地址
	DMA_InitStructure.DMA_Memory0BaseAddr = mar;	//DMA 存储器0地址
	DMA_InitStructure.DMA_DIR = DMA_DIR_MemoryToPeripheral; //存储器到外设模式
	DMA_InitStructure.DMA_BufferSize = ndtr;		//数据传输量 
	DMA_InitStructure.DMA_PeripheralInc = DMA_PeripheralInc_Disable; //外设非增量模式
	DMA_InitStructure.DMA_MemoryInc = DMA_MemoryInc_Enable; //存储器增量模式
	DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_Byte; //外设数据长度:8位
	DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_Byte; //存储器数据长度:8位
	DMA_InitStructure.DMA_Mode = DMA_Mode_Normal;	// 使用普通模式 
	DMA_InitStructure.DMA_Priority = DMA_Priority_Medium; //中等优先级
	DMA_InitStructure.DMA_FIFOMode = DMA_FIFOMode_Disable;
	DMA_InitStructure.DMA_FIFOThreshold = DMA_FIFOThreshold_Full;
	DMA_InitStructure.DMA_MemoryBurst = DMA_MemoryBurst_Single; //存储器突发单次传输
	DMA_InitStructure.DMA_PeripheralBurst = DMA_PeripheralBurst_Single; //外设突发单次传输
	DMA_Init(DMA_Streamx, &DMA_InitStructure);		//初始化DMA Stream

	DMA_ITConfig(DMA1_Stream6,  DMA_IT_TC, ENABLE);

	NVIC_InitStructure.NVIC_IRQChannel = DMA1_Stream6_IRQn; //串口1中断通道
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 3; //抢占优先级3
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3; //子优先级3
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE; //IRQ通道使能
	NVIC_Init(&NVIC_InitStructure); 				//根据指定的参数初始化VIC寄存器、  

}


/****************************************************************************
* 名	 称: void USER_DMA_Enable(DMA_Stream_TypeDef *DMA_Streamx,u16 ndtr)
* 功	 能：开启一次DMA传输
* 入口参数：DMA_Streamx:DMA数据流,DMA1_Stream0~7/DMA2_Stream0~7 
			ndtr:数据传输量 
* 返回参数：无
* 说	 明： 
****************************************************************************/
void USER_DMA_Enable(DMA_Stream_TypeDef * DMA_Streamx, u16 ndtr)
{

	DMA_Cmd(DMA_Streamx, DISABLE);					//关闭DMA传输 

	while (DMA_GetCmdStatus(DMA_Streamx) != DISABLE)
		{
		} //确保DMA可以被设置  

	DMA_SetCurrDataCounter(DMA_Streamx, ndtr);		//数据传输量  

	DMA_Cmd(DMA_Streamx, ENABLE);					//开启DMA传输 
}



void DMA1_Stream6_IRQHandler(void)
{
	// Check if the transfer complete flag is set
	if (DMA1->HISR & DMA_HISR_TCIF6) // Check for transfer complete flag in high ISR
		{
		// Clear the transfer complete flag
		DMA1->HIFCR 		|= DMA_HIFCR_CTCIF6;

		// Handle the transfer complete event (e.g., disable the DMA stream)
		DMA1_Stream6->CR	&= ~DMA_SxCR_EN;		// Disable the stream after completion

	
		}

}

























